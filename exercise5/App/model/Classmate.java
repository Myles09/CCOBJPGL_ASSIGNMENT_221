package model;

class Classmate {

    private String gender;
    private String scent;

    public String getgender() {
        return this.gender;
    }

    public void setgender(String gender) {
        this.gender = gender;
    }

    public String getscent() {
        return this.scent;
    }

    public void setscent(String scent) {
        this.scent = scent;
    }
}